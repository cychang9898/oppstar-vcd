#!/usr/bin/env node
'use strict';

const $version = () => '$version Generated by VerilatedVcd $end\n';

const $date = () => '$date Wed Sep 18 22:59:07 2019 $end\n';

const $timescale = () => '$timescale 1ns $end';

const randomScope = () => {
  return {
    top: {
      clock: '"}G',
      fruit: {
        point: 'u)'
      },
      leaf: {
        counter: '{u'
      }
    }
  };
};

const definitions = () => '';

const simulation = () => '';


const main = seed => {
  const scope = randomScope(seed);
  return (
    $version() +
    $date() +
    $timescale() +
    definitions(scope) +
    '$enddefinitions $end\n' +
    simulation(scope)
  );
};

console.log(main());
